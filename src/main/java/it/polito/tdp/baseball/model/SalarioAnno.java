package it.polito.tdp.baseball.model;

public class SalarioAnno {
	
	private double salario;
	private int anno;
	
	public SalarioAnno(double salario, int anno) {
		super();
		this.salario = salario;
		this.anno = anno;
	}

	public double getSalario() {
		return salario;
	}

	public int getAnno() {
		return anno;
	}
	
	
}
